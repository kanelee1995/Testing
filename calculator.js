const calculator = {
    add: add = (num1, num2) =>  {
        return num1 + num2;
    },
    subtract: subtract = (num1, num2) =>  {
        return num1 - num2;
    },
    multiply: multiply = (num1, num2) =>  {
        return num1 * num2;
    },
    divide: divide = (num1, num2) =>  {
        return num1 / num2;
    },
}

module.exports = calculator;
